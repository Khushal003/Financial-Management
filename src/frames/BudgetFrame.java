    private void addCategoryButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addCategoryButtonActionPerformed
        try {
            String name = nameTextField.getText().toUpperCase();
            String category = categoryComboBox.getSelectedItem().toString();
            String amount = amountTextField.getText();
            if (name.isEmpty()||category.isEmpty() || amount.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Enter valid input", "Person", JOptionPane.INFORMATION_MESSAGE);
            } else {

                Statement smt = con.createStatement();
                smt.execute("Insert into budget(name,category,amount) values ('"+name+"','" + category + "'," + amount + ")");
                JOptionPane.showMessageDialog(this,"Record Submitted Successfully","Project",JOptionPane.INFORMATION_MESSAGE);
                smt.close();
                //====to show table of jdbc
                setTableData();
                resetData();
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex, "Invalid option", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_addCategoryButtonActionPerformed

