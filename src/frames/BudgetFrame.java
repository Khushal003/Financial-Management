    private void deleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteButtonActionPerformed
          try {
              String name = nameTextField.getText().toUpperCase();
            if (id == null||name.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Please Select Record To Delete ", "Project ", JOptionPane.ERROR_MESSAGE);
            } else {

                Statement smt = con.createStatement();
                smt.execute("delete from budget where id = " + id);
                JOptionPane.showMessageDialog(this, "Record Deleted", "Project", JOptionPane.INFORMATION_MESSAGE);
                smt.close();
                setTableData();
                resetData();

            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex, "Project", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_deleteButtonActionPerformed

    private void budgetTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_budgetTableMouseClicked
          try {
            id = budgetTable.getValueAt(budgetTable.getSelectedRow(), 0).toString();
            Statement smt = con.createStatement();
            ResultSet rs = smt.executeQuery("select * from budget where id = " + id);
            
            if (rs.next()) {
                nameTextField.setText(rs.getString("name"));
                categoryComboBox.setSelectedIndex(0);
                amountTextField.setText(rs.getString("amount"));
            }
            rs.close();
            smt.close();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e, "Record Envalid", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_budgetTableMouseClicked

