   private void setTableData() {
        try {
            Statement smt = con.createStatement();
            ResultSet rs = smt.executeQuery("select * from budget ");
            rs.last();
            int lastRow = rs.getRow();
            String[] col = {"ID","NAME","CATEGORY","AMOUNT"};
            int row = rs.getRow();
            String[][] data = new String[lastRow][col.length];
            rs.beforeFirst();
            int i = 0;
            while (rs.next()) {
                data[i][0] = rs.getInt("id") + "";
                data[i][1] = rs.getString("name");
                data[i][2] = rs.getString("category");
                data[i][3] = rs.getInt("amount") + "";
                i++;
            }
            DefaultTableModel model1 = new DefaultTableModel(data, col);
            budgetTable.setModel(model1);

        } catch (Exception e) {
        }
    }
